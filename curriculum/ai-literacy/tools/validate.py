def _load_yaml(path, errors):
    try:
        return yaml.safe_load(path.read_text(encoding="utf-8"))
    except yaml.YAMLError as exc:
        errors.append("%s: YAML inválido: %s" % (path, exc))
        return None


def _check_catalog_shape(catalog, errors):
    """Integridade estrutural do catalog.yaml (índice canônico da trilha)."""
    prefix = "catalog.yaml"
    if not isinstance(catalog, dict):
        errors.append("%s: catálogo deve ser um objeto YAML" % prefix)
        return None
    for key in ("schemaVersion", "contentVersion", "track", "skills", "modules", "lessons"):
        if key not in catalog:
            errors.append("%s: chave obrigatória ausente: %s" % (prefix, key))
    if errors:
        return None
    return catalog


def _index_by_id(items, what, errors):
    index = {}
    for item in items or []:
        item_id = item.get("id") if isinstance(item, dict) else None
        if not item_id:
            errors.append("catalog.yaml: %s sem id" % what)
            continue
        if item_id in index:
            errors.append("catalog.yaml: id de %s duplicado: %s" % (what, item_id))
        index[item_id] = item
    return index


def _detect_prereq_cycles(lesson_index, errors):
    """Detecta ciclos no grafo de pré-requisitos (DFS com três cores)."""
    WHITE, GRAY, BLACK = 0, 1, 2
    color = {lesson_id: WHITE for lesson_id in lesson_index}

    def visit(lesson_id, stack):
        color[lesson_id] = GRAY
        stack.append(lesson_id)
        for prereq in lesson_index[lesson_id].get("prerequisites") or []:
            if prereq not in lesson_index:
                continue  # referência inexistente é reportada em outra checagem
            if color[prereq] == GRAY:
                cycle = stack[stack.index(prereq):] + [prereq]
                errors.append("catalog.yaml: ciclo de pré-requisitos detectado: %s" % " -> ".join(cycle))
            elif color[prereq] == WHITE:
                visit(prereq, stack)
        stack.pop()
        color[lesson_id] = BLACK

    for lesson_id in lesson_index:
        if color[lesson_id] == WHITE:
            visit(lesson_id, [])


def _check_activity_evaluation_references(lesson, lesson_file, errors):
    """Checks determinísticos devem referenciar ids declarados em data."""
    prefix = "%s (%s)" % (lesson_file, lesson.get("id", "?"))
    for activity in lesson.get("activities") or []:
        act_id = activity.get("id", "?")
        act_type = activity.get("type", "?")
        data = activity.get("data") or {}
        evaluation = activity.get("evaluation") or {}
        if not isinstance(data, dict) or not isinstance(evaluation, dict):
            continue  # erro estrutural já reportado pelo schema

        def ids_of(key):
            return {entry.get("id") for entry in data.get(key) or [] if isinstance(entry, dict)}

        if act_type == "choice":
            known = ids_of("options")
            for ref in evaluation.get("correctOptionIds") or []:
                if ref not in known:
                    errors.append("%s: atividade %s referencia opção inexistente em correctOptionIds: %s" % (prefix, act_id, ref))
        elif act_type == "sort":
            known = ids_of("items")
            order = evaluation.get("expectedOrder") or []
            if set(order) != known:
                errors.append("%s: atividade %s: expectedOrder deve ser uma permutação dos ids de data.items" % (prefix, act_id))
        elif act_type == "missing_context":
            known = ids_of("contextOptions")
            for key in ("requiredContextIds", "optionalContextIds"):
                for ref in evaluation.get(key) or []:
                    if ref not in known:
                        errors.append("%s: atividade %s referencia contexto inexistente em %s: %s" % (prefix, act_id, key, ref))
        elif act_type == "safety_classification":
            known = ids_of("items")
            classification = evaluation.get("classification") or {}
            if set(classification.keys()) != known:
                errors.append("%s: atividade %s: classification deve classificar exatamente os ids de data.items" % (prefix, act_id))
        elif act_type == "prompt_builder":
            known = ids_of("fields")
            for ref in (evaluation.get("fields") or {}).keys():
                if ref not in known:
                    errors.append("%s: atividade %s avalia campo inexistente em data.fields: %s" % (prefix, act_id, ref))
        elif act_type == "output_comparison":
            output_ids = ids_of("outputs")
            criterion_ids = ids_of("criteria")
            if evaluation.get("betterOutputId") not in output_ids:
                errors.append("%s: atividade %s: betterOutputId inexistente: %s" % (prefix, act_id, evaluation.get("betterOutputId")))
            for ref in evaluation.get("requiredCriterionIds") or []:
                if ref not in criterion_ids:
                    errors.append("%s: atividade %s referencia critério inexistente: %s" % (prefix, act_id, ref))
        elif act_type == "rubric_review":
            known = ids_of("criteria")
            for ref in (evaluation.get("expectedVerdicts") or {}).keys():
                if ref not in known:
                    errors.append("%s: atividade %s avalia critério inexistente em data.criteria: %s" % (prefix, act_id, ref))


def validate_track(track_dir):
    """Valida a trilha inteira. Retorna (errors, ready_lessons, catalog)."""
    track_dir = Path(track_dir)
    errors = []

    catalog_path = track_dir / "catalog.yaml"
    if not catalog_path.is_file():
        return ["catalog.yaml não encontrado em %s" % track_dir], [], None
    catalog = _load_yaml(catalog_path, errors)
    catalog = _check_catalog_shape(catalog, errors)
    if catalog is None:
        return errors, [], None

    skill_index = _index_by_id(catalog.get("skills"), "skill", errors)
    module_index = _index_by_id(catalog.get("modules"), "módulo", errors)
    lesson_index = _index_by_id(catalog.get("lessons"), "lição", errors)

    # Integridade das entradas do catálogo.
    for lesson_id, entry in lesson_index.items():
        for key in ("moduleId", "title", "objective", "estimatedMinutes", "prerequisites", "skillIds", "status"):
            if key not in entry:
                errors.append("catalog.yaml: lição %s sem campo obrigatório: %s" % (lesson_id, key))
        module_id = entry.get("moduleId")
        if module_id and module_id not in module_index:
            errors.append("catalog.yaml: lição %s referencia módulo inexistente: %s" % (lesson_id, module_id))
        for skill_id in entry.get("skillIds") or []:
            if skill_id not in skill_index:
                errors.append("catalog.yaml: lição %s referencia skill inexistente: %s" % (lesson_id, skill_id))
        for prereq in entry.get("prerequisites") or []:
            if prereq not in lesson_index:
                errors.append("catalog.yaml: lição %s referencia pré-requisito inexistente: %s" % (lesson_id, prereq))
        if entry.get("estimatedMinutes") not in VALID_DURATIONS:
            errors.append("catalog.yaml: lição %s com duração fora de {3,4,5}: %r" % (lesson_id, entry.get("estimatedMinutes")))
        if entry.get("status") not in VALID_STATUSES:
            errors.append("catalog.yaml: lição %s com status inválido: %r (válidos: %s)" % (lesson_id, entry.get("status"), ", ".join(VALID_STATUSES)))

    _detect_prereq_cycles(lesson_index, errors)

    # Carrega arquivos de lição.
    lesson_files = {}
    modules_dir = track_dir / "modules"
    if modules_dir.is_dir():
        for path in sorted(modules_dir.glob("*/*.yaml")):
            lesson_files[path] = None  # preenchido após parse

    resolver = SchemaResolver(track_dir / "schemas")
    parsed_lessons = {}
    activity_ids = {}
    for path in sorted(lesson_files):
        rel = path.relative_to(track_dir)
        data = _load_yaml(path, errors)
        if data is None:
            continue
        schema_errors = validate_against_schema(data, "lesson.schema.json", resolver)
        for err in schema_errors:
            errors.append("%s: %s" % (rel, err))
        if schema_errors:
            continue
        lesson_id = data["id"]
        if lesson_id in parsed_lessons:
            errors.append("%s: id de lição duplicado: %s (também em %s)" % (rel, lesson_id, parsed_lessons[lesson_id][0]))
        parsed_lessons[lesson_id] = (rel, data)
        for activity in data.get("activities") or []:
            act_id = activity.get("id")
            if act_id in activity_ids:
                errors.append("%s: id de atividade duplicado: %s (também em %s)" % (rel, act_id, activity_ids[act_id]))
            activity_ids[act_id] = rel
        _check_activity_evaluation_references(data, rel, errors)
        for skill_id in data.get("skillIds") or []:
            if skill_id not in skill_index:
                errors.append("%s: lição %s referencia skill inexistente: %s" % (rel, lesson_id, skill_id))
        for activity in data.get("activities") or []:
            skill_id = activity.get("skillId")
            if skill_id and skill_id not in skill_index:
                errors.append("%s: atividade %s referencia skill inexistente: %s" % (rel, activity.get("id"), skill_id))
        if data.get("moduleId") not in module_index:
            errors.append("%s: lição %s referencia módulo inexistente: %s" % (rel, lesson_id, data.get("moduleId")))
        for prereq in data.get("prerequisites") or []:
            if prereq not in lesson_index:
                errors.append("%s: lição %s referencia pré-requisito inexistente: %s" % (rel, lesson_id, prereq))
        own_activity_ids = {a.get("id") for a in data.get("activities") or []}
        for required in (data.get("completion") or {}).get("requiredActivityIds") or []:
            if required not in own_activity_ids:
                errors.append("%s: lição %s exige atividade inexistente em completion.requiredActivityIds: %s" % (rel, lesson_id, required))

    # Consistência catálogo <-> arquivos.
    file_lesson_ids = set(parsed_lessons)
    for lesson_id, entry in lesson_index.items():
        status = entry.get("status")
        if status == "ready" and lesson_id not in file_lesson_ids:
            errors.append("catalog.yaml: lição %s marcada como ready mas sem arquivo em modules/" % lesson_id)
        if status == "planned" and lesson_id in file_lesson_ids:
            errors.append("catalog.yaml: lição %s tem arquivo mas está marcada como planned — promova para ready" % lesson_id)
        if lesson_id in file_lesson_ids:
            rel, data = parsed_lessons[lesson_id]
            for field in ("moduleId", "title", "estimatedMinutes", "prerequisites", "skillIds"):
                if entry.get(field) != data.get(field):
                    errors.append("catalog.yaml: lição %s diverge do arquivo %s no campo %s (%r != %r)" % (lesson_id, rel, field, entry.get(field), data.get(field)))
    for lesson_id, (rel, _data) in parsed_lessons.items():
        if lesson_id not in lesson_index:
            errors.append("%s: lição %s não está listada no catalog.yaml" % (rel, lesson_id))

    ready = [data for lesson_id, (_rel, data) in sorted(parsed_lessons.items()) if lesson_index.get(lesson_id, {}).get("status") == "ready"]
    return errors, ready, catalog


