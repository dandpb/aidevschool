def compile_track(track_dir, outdir):
    """Compila as lições válidas para o read model tipado. Retorna (errors, output_path)."""
    errors, ready_lessons, catalog = validate_track(track_dir)
    if errors:
        return errors, None
    outdir = Path(outdir)
    outdir.mkdir(parents=True, exist_ok=True)
    out_path = outdir / "lessons.ts"

    ready_ids = {lesson["id"] for lesson in ready_lessons}
    entries = []
    for entry in catalog.get("lessons") or []:
        entries.append(
            {
                "id": entry["id"],
                "moduleId": entry["moduleId"],
                "title": entry["title"],
                "estimatedMinutes": entry["estimatedMinutes"],
                "prerequisites": entry.get("prerequisites") or [],
                "skillIds": entry.get("skillIds") or [],
                "status": entry["status"],
                "hasContent": entry["id"] in ready_ids,
            }
        )
    modules_payload = []
    for module in sorted(catalog.get("modules") or [], key=lambda item: item.get("order", 0)):
        modules_payload.append(
            {
                "id": module["id"],
                "slug": module["slug"],
                "title": module["title"],
                "order": module["order"],
                "skillIds": module.get("skillIds") or [],
                "lessons": [entry for entry in entries if entry["moduleId"] == module["id"]],
            }
        )
    skills_payload = [
        {"id": skill["id"], "title": skill["title"], "description": skill["description"]}
        for skill in catalog.get("skills") or []
    ]
    track_payload = {key: catalog["track"][key] for key in ("id", "title", "audience", "promise", "language")}

    def as_ts(payload):
        return json.dumps(payload, ensure_ascii=False, indent=2)

    content = (
        GENERATED_HEADER
        + "\n// Read model tipado da trilha ai-literacy. Fonte canônica: curriculum/ai-literacy/.\n"
        + "// Regenere com: python3 curriculum/ai-literacy/tools/validate.py --compile <outdir>\n\n"
        + TS_TYPES
        + "\nexport const contentVersion: string = "
        + json.dumps(str(catalog.get("contentVersion")), ensure_ascii=False)
        + "\n\nexport const track: Track = "
        + as_ts(track_payload)
        + "\n\nexport const modules: ModuleDefinition[] = "
        + as_ts(modules_payload)
        + "\n\nexport const skills: SkillDefinition[] = "
        + as_ts(skills_payload)
        + "\n\nexport const lessons: LessonDefinition[] = "
        + as_ts(ready_lessons)
        + "\n"
    )
    out_path.write_text(content, encoding="utf-8")
    return [], out_path


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main(argv=None):
    parser = argparse.ArgumentParser(description="Valida a trilha ai-literacy e opcionalmente compila o read model TypeScript.")
    parser.add_argument("--track", default=str(TRACK_DIR), help="diretório da trilha (padrão: curriculum/ai-literacy)")
    parser.add_argument("--compile", metavar="OUTDIR", default=None, help="gera OUTDIR/lessons.ts após validação bem-sucedida")
    args = parser.parse_args(argv)

    track_dir = Path(args.track)
    errors, ready, catalog = validate_track(track_dir)
    out_path = None
    if not errors and args.compile:
        errors, out_path = compile_track(track_dir, args.compile)

    if errors:
        print("VALIDAÇÃO FALHOU — %d erro(s):" % len(errors), file=sys.stderr)
        for err in errors:
            print("  - %s" % err, file=sys.stderr)
        return 1

    planned = sum(1 for entry in (catalog or {}).get("lessons", []) if entry.get("status") == "planned")
    print("OK: %d lição(ões) ready validadas, %d planned (sem arquivo exigido)." % (len(ready), planned))
    if out_path is not None:
        print("Read model gerado: %s" % out_path)
    return 0


if __name__ == "__main__":
    sys.exit(main())
